<u><b>What is it?</b></u>
<p>
<b>Simply Inventory System</b> is a very simple inventory system targeting for small business with small warehouse 
and it is designed for fashion and clothing industry. 
It is a browser based applications which utilized AJAX to let user input color/size on 
a two dimension table. Currenty, it contains purchase, sales, inventory inquiry and inventory reports.
</p>
